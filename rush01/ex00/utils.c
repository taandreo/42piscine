#include <unistd.h>

void	error(void)
{
	write(1, "Error\n", 6);
}

void	zero_mt(int (*m)[4])
{
	int	i;
	int	j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			m[i][j] = 0;
			j++;
		}
	i++;
	}
}
