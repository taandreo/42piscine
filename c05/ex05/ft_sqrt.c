int	ft_sqrt(int nb)
{
	int	n;

	n = 0;
	if (nb < 0)
		return (0);
	while (n * n <= nb)
	{
		if (n >= 46341)
			return (0);
		if (n * n == nb)
			return (n);
		if (n * n > nb)
			return (0);
		n++;
	}
	return (0);
}
